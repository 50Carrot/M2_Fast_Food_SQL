Se ha creado una base de datos denominada fast_food_db con varias tablas para gestionar clientes, sucursales, mensajeros, orígenes de orden, tipos de pago, categorías de productos, departamentos, productos, empleados, órdenes y artículos de órdenes. Estas tablas se diseñaron para capturar información relevante sobre cada aspecto de las operaciones de la empresa.

Resultados y Consultas
Eficiencia de los Mensajeros
Pregunta: ¿Cuál es el tiempo promedio desde el despacho hasta la entrega de los pedidos gestionados por todo el equipo de mensajería?
sql
Copy code
-- Calculate the average time from dispatch to delivery for all messengers
SELECT AVG(DATEDIFF(MINUTE, o.dispatch_datetime, o.delivery_datetime)) AS 'Average Delivery Time (minutes)'
FROM orders o
INNER JOIN messengers m ON o.messenger_id = m.messenger_id;
GO
Resultado: El tiempo promedio de entrega es de 30 minutos.
Análisis de Ventas por Origen de Orden
Pregunta: ¿Qué canal de ventas genera más ingresos?
sql
Copy code
-- Total sales by sales channel
SELECT s.origin_name, SUM(o.total_amount) AS 'Total Sales'
FROM orders o
INNER JOIN order_origins s ON o.order_origin_id = s.order_origin_id
GROUP BY s.origin_name
ORDER BY 'Total Sales' DESC;
GO
Resultado: Los datos indican un total de ventas de 21,694.04 en el origen de ventas.
