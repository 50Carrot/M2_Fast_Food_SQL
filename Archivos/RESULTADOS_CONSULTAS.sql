Análisis de Fidelidad del Cliente
Pregunta: ¿Qué porcentaje de clientes son recurrentes versus nuevos clientes cada mes?
sql
Copy code
-- Percentage of recurring versus new customers
WITH CustomerOrders AS (
    SELECT customer_id, COUNT(*) AS order_count
    FROM orders
    GROUP BY customer_id
)
SELECT 
    CASE 
        WHEN order_count > 1 THEN 'Recurring'
        ELSE 'New'
    END AS 'Customer Type',
    COUNT(*) AS 'Number of Customers',
    (COUNT(*) * 100.0 / (SELECT COUNT(*) FROM CustomerOrders)) AS 'Percentage'
FROM CustomerOrders
GROUP BY 
    CASE 
        WHEN order_count > 1 THEN 'Recurring'
        ELSE 'New'
    END;
GO
Resultado:
Recurring: 5 customers (100%)

